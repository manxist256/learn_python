# 1. for loop on a list of strings
game = ["Hello", "Brow"]
print(game[0])
for x in game:
    print(x)

print("---------------")

# 2. for loop on a list of numbers
nums = [1, 3, 7, 1000]
for x in nums:
    print(x)

print("---------------")

# 3. for loop on a list using indexes
grass = ["1", "Hello", "@"]
for x in range(len(grass)):
    print(grass[x])

print("---------------")

# 4. for loop on a string
stringy = "brow"
print(stringy[0])
for x in stringy:
    print(x)

print("---------------")

# 5. for loop on a string using indexes
for x in range(len(stringy)):
    print(stringy[x])

print("---------------")
