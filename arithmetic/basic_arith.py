# addition
print(10 + 2)
# subtraction
print(10 - 2)
# multiplication
print(10 * 2)
# division type 1 - produces result in floating type number
print(10 / 3)
# division type 2 - it internally does above division type 1 and then does a rounding-floor on the resultant.
print(10 // 3) # result -> 3
# modulo
print(10 % 3)
# exponent
print(10 ** 3) # does 10 to the power 3
print(8 ** 7) # does 8 power 7
