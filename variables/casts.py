# This is a integer variable
xyz = 25
print(type(xyz))
print(xyz)

print("---------------")

# 1. Cast int to float
abc = float(xyz)
print(type(abc))
print(abc)

print("---------------")

# 2. Cast float to int
andx = 9.2
dex = int(andx)
print(type(dex))
print(dex)

print("---------------")

# 3. Cast String to int
str1 = "25"
str11 = int(str1)
print(type(str11))
print(str11)

print("---------------")

# 4. Cast String to float
str2 = "25.2"
str22 = float(str2)
print(type(str22))
print(str22)

